        PROCEDURE INSERT_USERNAME(PNUSERNAME VARCHAR2,PNPASSWORD VARCHAR2,PNIDENTIFICATION VARCHAR2,PNID_USERTYPE NUMBER) IS

           BEGIN
             INSERT INTO USERNAME(USERNAME,PASSWORD,IDENTIFICATION,ID_USERTYPE)
             VALUES (PNUSERNAME,PNPASSWORD,PNIDENTIFICATION,PNID_USERTYPE);

           END INSERT_USERNAME;

           ---------------------------------------------------------------------------------
    PROCEDURE SET_USERNAME(PNIDENTIFICATION VARCHAR2, PNNEW_VALUE VARCHAR2) IS
      VMENERROR                             EXCEPTION;
         BEGIN
             UPDATE USERNAME SET USERNAME= PNNEW_VALUE WHERE IDENTIFICATION=PNIDENTIFICATION;
             IF SQL%NOTFOUND THEN
                    RAISE VMENERROR;
            END IF;
            EXCEPTION 
               WHEN VMENERROR THEN
                 DBMS_OUTPUT.PUT_LINE('THE ELEMENT DOES NOT EXIST IN THE DATABASE.');
         END SET_USERNAME;

         ---------------------------------------------------------------------------------
    PROCEDURE SET_IDENTIFICATION(PNIDENTIFICATION VARCHAR2, PNNEW_VALUE VARCHAR2) IS
