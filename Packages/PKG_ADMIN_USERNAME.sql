         ---------------------------------------------------------------------------------
    PROCEDURE SET_PASSWORD(PNIDENTIFICATION VARCHAR2, PNOLD_VALUE VARCHAR2, PNNEW_VALUE VARCHAR2) IS
      VMENERROR                             EXCEPTION;
         BEGIN
             UPDATE USERNAME SET PASSWORD= PNNEW_VALUE WHERE IDENTIFICATION=PNIDENTIFICATION;
             INSERT INTO BINNACLE (ID_BINNACLE,ID_PERSON,PREVIOUS_PASSWORD,CURRENT_PASSWORD,PASS_CHANGE_DATE)
                    VALUES(S_BINNACLE.NEXTVAL,PNIDENTIFICATION,PNOLD_VALUE,PNNEW_VALUE,SYSDATE);
             IF SQL%NOTFOUND THEN
                    RAISE VMENERROR;
            END IF;
            EXCEPTION 
               WHEN VMENERROR THEN
                 DBMS_OUTPUT.PUT_LINE('THE ELEMENT DOES NOT EXIST IN THE DATABASE.');
         END SET_PASSWORD;
